
function plot_kernel_weight()

  addpath('./savefigure/');

  [xFilenameList,yFilenameList,auc,auprc,acc,f1,precision,recall] = textread('../ResultsMKL/results','%s %s %s %s %s %s %s %s','headerlines',1);
  
  figure1 = figure('visible','off');
  set(figure1, 'PaperUnits', 'centimeters');
  set(figure1, 'Position',[0 0 1000 500]);
           
  W = [];
  for i=1:size(xFilenameList,1)
    xFilename = xFilenameList{i};
    [X] = dlmread(sprintf('../Data/tcdb.all.w%s',regexprep(xFilename,'.*\.','')));
    W = [W,X];
  end
  bar(W);
  lgd = legend(xFilenameList);
  str = [...
      'TB                     ';...
      'TICoils                ';...
      'TIGene3D               ';...
      'TIHamap                ';...
      'TIPANTHER              ';...
      'TIPfam                 ';...
      'TIPhobius              ';...
      'TIPIRSF                ';...
      'TIPRINTS               ';...
      'TIProDom               ';...
      'TIProSitePatterns      ';...
      'TIProSiteProfiles      ';...
      'TISignalP_EUK          ';...
      'TISignalP_GRAM_NEGATIVE';...
      'TISignalP_GRAM_POSITIVE';...
      'TISMART                ';...
      'TISUPERFAMILY          ';...
      'TITIGRFAM              ';...
      'TITMHMM                '];
  t=text(1:19,ones(1,19)*0,str);
  set(t,'HorizontalAlignment','right','VerticalAlignment','bottom','Rotation',-75);
  set(lgd, 'interpreter','latex','Position', [0.7,0.5,0.0,0.0]);
  xlabel('Base Kernels'); 
  ylabel('Kernel weight');
  title('')
  export_fig(sprintf('../PlotsMKL/kernel_weights.jpg'))

end


